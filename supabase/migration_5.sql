alter table profiles add column if not exists username text;

create unique index if not exists profiles_username_unique
  on profiles (lower(username))
  where username is not null;

-- За вече съществуващи акаунти (напр. първия администратор, ако си го
-- създал преди тази миграция) - попълва временно потребителско име от
-- частта преди "@" на текущия му имейл, за да не остане празно.
-- Можеш да го смениш по-късно от "Акаунти".
update profiles
set username = split_part(email, '@', 1)
where username is null and email is not null;

-- Забележка за вход с вече съществуващи акаунти (създадени преди тази
-- миграция): те влизат по стария начин - с реалния имейл, с който са
-- създадени. Всички НОВИ акаунти (създадени от "Акаунти" след тази
-- миграция) вече използват само потребителско име - формата за вход
-- разпознава автоматично дали си въвел имейл (съдържа "@") или
-- потребителско име.
