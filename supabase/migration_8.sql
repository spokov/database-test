-- Забележка: тази миграция приема, че параметрите в групите "tanita" и
-- "body" все още са в първоначалната подредба, с която бяха създадени
-- (sort_order 1..10 за tanita, 1..5 за body) - т.е. не са пренареждани
-- междувременно (не е имало откъде - няма бутон за пренареждане, само за
-- преименуване/добавяне/триене). Ако някой от тях вече е бил преименуван
-- ръчно от "Настройки", това ще бъде презаписано с новото име по-долу.
-- Ако нещо не съвпадне с очакваното след пускането, най-лесно е да го
-- коригираш направо от "Настройки" - историята остава свързана към
-- параметъра независимо от името му, нищо не се губи.

-- ---------- Танита измервания: преименуване + ново подреждане ----------
update parameters p
set name = v.new_name, sort_order = v.new_order
from (values
  (1,  'Тегло',                    1),
  (2,  'BMI (%)',                  2),
  (3,  'Мазнини (%)',              3),
  (7,  'Вътрешни мазнини',         4),
  (4,  'Мускулна маса (кг)',       5),
  (10, 'Индекс на тялото',         6),
  (5,  'Костна маса (кг)',         7),
  (8,  'Базов метаболизъм (ккал)', 8),
  (9,  'Метаболитна възраст',      9),
  (6,  'Вода в тялото (%)',        10)
) as v(old_order, new_name, new_order)
where p.category = 'tanita' and p.sort_order = v.old_order;

-- ---------- Мерки на тялото: преименуване + ново подреждане ----------
update parameters p
set name = v.new_name, sort_order = v.new_order
from (values
  (1, 'Обиколка Бюст (см)',  1),
  (4, 'Обиколка Ръка (см)',  2),
  (2, 'Обиколка Талия (см)', 3),
  (3, 'Обиколка Ханш (см)',  5),
  (5, 'Обиколка Бедро (см)', 6)
) as v(old_order, new_name, new_order)
where p.category = 'body' and p.sort_order = v.old_order;

-- ---------- Мерки на тялото: 3 нови параметъра (групата расте от 5 на 8) ----------
insert into parameters (name, value_type, category, sort_order)
select v.name, 'number', 'body', v.sort_order
from (values
  ('Обиколка Корем (см)',  4),
  ('Обиколка Коляно (см)', 7),
  ('Тегло (кг)',           8)
) as v(name, sort_order)
where not exists (
  select 1 from parameters where category = 'body' and name = v.name
);
